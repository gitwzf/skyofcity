package model;

public class Simplestyle {
     	public String key="";
     	public String style="";
     	
		public String getKey() {
			return key;
		}
		public void setKey(String key) {
			this.key = key;
		}
		public String getStyle() {
			return style;
		}
		public void setStyle(String style) {
			this.style = style;
		}
     	
}
